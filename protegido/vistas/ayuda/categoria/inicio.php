<p>Aquí podrás ver un listado de las categorías registradas en el club.</p>
<p>En la columna de opciones podrás ejecutar cualquiera de las siguientes de opciones: </p>
<ol>
    <li><b>Ver:</b> Podrás ver en detalle la información de la categoría</li> 
    <li><b>Editar:</b> Podrás modificar la información de la categoría</li> 
    <li><b>Eliminar:</b> Podrás eliminar la categoría</li> 
</ol>
<p><b>Nota: </b> Ten en cuenta que si la categoría se encuentra ligada a otra información no podrá ser eliminada</p>    
